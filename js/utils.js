export function formatCurrency(value) {
  return new Intl.NumberFormat('pt-BR', {
    style: 'currency',
    currency: 'BRL',
  }).format(value);
}

export function formatDate(dateStr) {
  const d = new Date(dateStr + 'T00:00:00');
  return d.toLocaleDateString('pt-BR');
}

export function formatDateShort(dateStr) {
  const d = new Date(dateStr + 'T00:00:00');
  return d.toLocaleDateString('pt-BR', { day: '2-digit', month: 'short' });
}

export function formatMonthYear(dateStr) {
  const d = new Date(dateStr + 'T00:00:00');
  return d.toLocaleDateString('pt-BR', { month: 'long', year: 'numeric' });
}

export function getMonthName(index) {
  const months = [
    'Janeiro', 'Fevereiro', 'Março', 'Abril', 'Maio', 'Junho',
    'Julho', 'Agosto', 'Setembro', 'Outubro', 'Novembro', 'Dezembro'
  ];
  return months[index] || '';
}

export function generateId() {
  return 'id_' + Date.now().toString(36) + '_' + Math.random().toString(36).substring(2, 9);
}

export function getTodayStr() {
  const d = new Date();
  return d.toISOString().split('T')[0];
}

export function getDateRange(filterType) {
  const now = new Date();
  let start, end;

  end = new Date(now.getFullYear(), now.getMonth() + 1, 0); // last day of current month

  switch (filterType) {
    case 'current':
      start = new Date(now.getFullYear(), now.getMonth(), 1);
      break;
    case 'previous':
      start = new Date(now.getFullYear(), now.getMonth() - 1, 1);
      end = new Date(now.getFullYear(), now.getMonth(), 0);
      break;
    case 'last3':
      start = new Date(now.getFullYear(), now.getMonth() - 2, 1);
      break;
    case 'last6':
      start = new Date(now.getFullYear(), now.getMonth() - 5, 1);
      break;
    case 'last12':
      start = new Date(now.getFullYear(), now.getMonth() - 11, 1);
      break;
    default:
      start = new Date(now.getFullYear(), now.getMonth(), 1);
  }

  return {
    start: start.toISOString().split('T')[0],
    end: end.toISOString().split('T')[0],
  };
}

export function debounce(fn, delay = 300) {
  let timer;
  return function (...args) {
    clearTimeout(timer);
    timer = setTimeout(() => fn.apply(this, args), delay);
  };
}

export function parseCurrencyInput(str) {
  if (!str) return 0;
  // Remove R$, dots (thousand sep), and replace comma with dot
  const cleaned = str.replace(/[R$\s.]/g, '').replace(',', '.');
  const val = parseFloat(cleaned);
  return isNaN(val) ? 0 : val;
}

export function escapeHtml(str) {
  const div = document.createElement('div');
  div.textContent = str;
  return div.innerHTML;
}

